function deriveClass(m: MaterialRow): ClassFilter {
  const p = m.petroload_score ?? 0;
  if (m.bio_based_flag && p <= 0.3) return "bio-based";
  if (m.transition_flag) return "bridge";
  if (m.petro_based_flag || m.synthetic_flag) {
    return p >= 0.8 ? "petrochemical" : "synthetic";
  }
  if (m.plant_based_flag && p <= 0.35) return "bio-based";
  if (p <= 0.2) return "bio-based";
  if (p <= 0.55) return "bridge";
  if (p <= 0.8) return "synthetic";
  return "petrochemical";
}

